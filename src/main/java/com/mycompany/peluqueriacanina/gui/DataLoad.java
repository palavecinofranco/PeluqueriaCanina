    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String petName = txtNombre.getText();
        String race = txtRaza.getText();
        String color = txtColor.getText();
        String observations = txtObservaciones.getText();
        String ownerName = txtNombreDueño.getText();
        String cellPhone = txtTel.getText();
        String allergic = cmbAlergico.getSelectedItem().toString();
        String specialAttention = cmbAtEspecial.getSelectedItem().toString();
        controller.save(petName,race,color,observations,ownerName,cellPhone,allergic,specialAttention);
        JOptionPane optionPane = new JOptionPane("Guardado correctamente");
        optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        JDialog dialog = optionPane.createDialog("Guardado con éxito");
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
        btnLimpiarActionPerformed(evt);
    }//GEN-LAST:event_btnGuardarActionPerformed


